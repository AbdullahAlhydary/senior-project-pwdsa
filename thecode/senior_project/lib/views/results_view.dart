

// ignore_for_file: deprecated_member_use

import 'dart:typed_data';

import 'package:flutter/material.dart';
import 'package:pdf/pdf.dart';
import 'package:pdf/widgets.dart' as pw;
import 'package:printing/printing.dart';
import 'package:senior_project/utilities/constants.dart';
import 'package:senior_project/utilities/dummy_data.dart';

class ResultsView extends StatefulWidget {
  const ResultsView({super.key});

  @override
  State<ResultsView> createState() => _ResultsViewState();
}

class _ResultsViewState extends State<ResultsView> {
  pw.TableRow _buildRow(String title, dynamic value) {
    return pw.TableRow(
      children: [
        pw.Padding(padding: const pw.EdgeInsets.all(6), child: pw.Text(title)),
        pw.Padding(
          padding: const pw.EdgeInsets.all(6),
          child: pw.Text(value.toString()),
        ),
      ],
    );
  }

  Future<Uint8List> generatePdf() async {
    final pdf = pw.Document();
    pdf.addPage(
      pw.MultiPage(
        pageFormat: PdfPageFormat.a4,
        margin: const pw.EdgeInsets.all(24),
        build: (context) => [
          pw.Text(
            "Water Injection Suitability Report",
            style: pw.TextStyle(fontSize: 22, fontWeight: pw.FontWeight.bold),
          ),
          pw.SizedBox(height: 8),
          pw.Text(
            "Generated on: ${DateTime.now()}",
            style: pw.TextStyle(fontSize: 10, color: PdfColors.grey700),
          ),
          pw.Divider(thickness: 1),
          pw.SizedBox(height: 10),
          pw.Text(
            "Input Parameters",
            style: pw.TextStyle(fontSize: 16, fontWeight: pw.FontWeight.bold),
          ),
          pw.SizedBox(height: 10),
          pw.Table(
            border: pw.TableBorder.all(color: PdfColors.grey400),
            columnWidths: {
              0: const pw.FlexColumnWidth(3),
              1: const pw.FlexColumnWidth(2),
            },
            children: [
              pw.TableRow(
                decoration: const pw.BoxDecoration(color: PdfColors.grey300),
                children: [
                  pw.Padding(
                    padding: const pw.EdgeInsets.all(6),
                    child: pw.Text(
                      "Parameter",
                      style: pw.TextStyle(fontWeight: pw.FontWeight.bold),
                    ),
                  ),
                  pw.Padding(
                    padding: const pw.EdgeInsets.all(6),
                    child: pw.Text(
                      "Value",
                      style: pw.TextStyle(fontWeight: pw.FontWeight.bold),
                    ),
                  ),
                ],
              ),
              _buildRow("MAIP (psi)", "maipValue"),
              _buildRow(
                "Required Injection Pressure (psi)",
                "injectionPressureValue",
              ),
              _buildRow("Permeability (mD)", "permeabilityValue"),
              _buildRow("Clay Fraction", "clayFractionValue"),
              _buildRow("TDS (mg/L)", "tdsValue"),
              _buildRow("Oil in Water (ppm)", "oilValue"),
              _buildRow("TSS (mg/L)", "tssValue"),
            ],
          ),
          pw.SizedBox(height: 20),
          pw.Text(
            "Analysis & Conclusion",
            style: pw.TextStyle(fontSize: 16, fontWeight: pw.FontWeight.bold),
          ),
          pw.SizedBox(height: 10),
          pw.Text(
            "Based on the provided parameters, the water is considered suitable for injection. "
            "This is because the Total Dissolved Solids (TDS), Total Suspended Solids (TSS), "
            "and Oil in Water values are within acceptable limits, which minimizes the risk of "
            "formation damage, plugging, and scaling. Additionally, the injection pressure is "
            "within the safe operating range relative to the MAIP, ensuring safe and efficient "
            "injection without fracturing the formation.",
            style: pw.TextStyle(fontSize: 12),
            textAlign: pw.TextAlign.justify,
          ),
          pw.SizedBox(height: 30),
          pw.Align(
            alignment: pw.Alignment.centerRight,
            child: pw.Text(
              "Generated by System",
              style: pw.TextStyle(fontSize: 10, color: PdfColors.grey600),
            ),
          ),
        ],
      ),
    );

    return pdf.save();
  }

  Future<void> generateAndPreviewPdf() async {
    await Printing.layoutPdf(onLayout: (format) async => await generatePdf());
  }

  @override
  Widget build(BuildContext context) {
    return Container(
      decoration: const BoxDecoration(
        gradient: LinearGradient(
          colors: [Color(0xFF050F2C), Color(0xFF0A1F44), Color(0xFF112D4E)],
          begin: Alignment.topLeft,
          end: Alignment.bottomRight,
        ),
      ),
      child: ListView.builder(
        padding: const EdgeInsets.all(12),
        itemCount: results.length,
        itemBuilder: (context, index) {
          return Card(
            elevation: 8,
            shadowColor: Colors.black54,
            shape: RoundedRectangleBorder(
              borderRadius: BorderRadius.circular(18),
            ),
            color: Colors.white.withOpacity(0.08),
            child: Padding(
              padding: const EdgeInsets.all(12),
              child: ListTile(
                contentPadding: EdgeInsets.zero,

                title: Text(
                  "Result ${index + 1}",
                  style: kBodyTextStyle.copyWith(
                    color: Colors.white,
                    fontWeight: FontWeight.bold,
                  ),
                ),

                subtitle: Padding(
                  padding: const EdgeInsets.only(top: 8),
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: results[index].details
                        .map(
                          (detail) => Padding(
                            padding: const EdgeInsets.only(bottom: 4),
                            child: Text(
                              detail,
                              style: const TextStyle(
                                color: Colors.white70,
                                fontSize: 13,
                              ),
                            ),
                          ),
                        )
                        .toList(),
                  ),
                ),

                trailing: Container(
                  decoration: BoxDecoration(
                    color: Colors.white.withOpacity(0.1),
                    borderRadius: BorderRadius.circular(12),
                  ),
                  child: IconButton(
                    onPressed: () async {
                      final pdfData = await generatePdf();
                      await Printing.layoutPdf(
                        onLayout: (format) async => pdfData,
                      );
                    },
                    icon: const Icon(Icons.download, color: Colors.white),
                  ),
                ),
              ),
            ),
          );
        },
      ),
    );
  }
}
